    public int[] array_shifter(int[] array,int shift)

    {

        int[] original = array;
        int[] reordered = new int[original.length];
        int shifta = shift;

        for(int i=0; i<original.length;i++)
            reordered[i] = original[(shift+i)%original.length];

        return reordered;
    }


